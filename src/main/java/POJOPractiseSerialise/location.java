package POJOPractiseSerialise;

public class location {
    private double lat;
    private double lng;

    public void setlat(Double lat) {
        this.lat = lat;
    }
    public Double getlat() {
        return lat;
    }
    public void setLng(Double lng) {
        this.lng = lng;
    }
    public Double getLng() {
        return lng;
    }
}
